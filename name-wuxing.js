  function strokeToElement(n) {
    const d = ((n % 10) + 10) % 10;
    if (d === 1 || d === 2) return "木";
    if (d === 3 || d === 4) return "火";
    if (d === 5 || d === 6) return "土";
    if (d === 7 || d === 8) return "金";
    return "水";
  }

  /** 未收錄字：以 Unicode 與常見結構粗估筆畫（僅備援） */
  function estimateStroke(ch) {
    const code = ch.codePointAt(0);
    if (code < 0x4e00 || code > 0x9fff) return { stroke: 8, estimated: true };
    // 粗估：依區段給 6–18 畫，避免全落同一五行
    const base = 6 + ((code * 7) % 13);
    return { stroke: base, estimated: true };
  }

  function getStroke(ch) {
    if (Object.prototype.hasOwnProperty.call(STROKES, ch)) {
      return { char: ch, stroke: STROKES[ch], estimated: false };
    }
    const est = estimateStroke(ch);
    return { char: ch, stroke: est.stroke, estimated: true };
  }

  function splitName(fullName) {
    const name = String(fullName || "")
      .replace(/\s+/g, "")
      .replace(/[·•．.]/g, "");
    if (!name) throw new Error("請輸入姓名。");

    // 允許 2–6 個漢字（含複姓）
    const chars = [...name];
    if (chars.length < 2 || chars.length > 6) {
      throw new Error("請輸入 2～6 個字的中文姓名。");
    }
    for (const ch of chars) {
      if (!/[\u4e00-\u9fff]/.test(ch)) {
        throw new Error("姓名請使用中文漢字。");
      }
    }

    let surname = "";
    let given = "";
    const two = chars.slice(0, 2).join("");
    if (COMPOUND_SURNAMES.includes(two) && chars.length >= 3) {
      surname = two;
      given = chars.slice(2).join("");
    } else {
      surname = chars[0];
      given = chars.slice(1).join("");
    }
    if (!given) throw new Error("請輸入完整姓名（含名字）。");
    return { full: name, surname, given, chars };
  }

